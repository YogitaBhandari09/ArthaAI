export function normalizeLang(code = "hi-IN") {
  const normalized = String(code || "").toLowerCase();
  if (normalized.startsWith("bn")) return "bn-IN";
  if (normalized.startsWith("ta")) return "ta-IN";
  return "hi-IN";
}

export function getLangCopy(code = "hi-IN") {
  return COPY[normalizeLang(code)] || COPY["hi-IN"];
}

